package com.khaling.rest.crud.restfulcrudservices.dto;


public class StudentDTO {

    private Long id;
    private String name;
    private Integer age;
    private String email;

    public StudentDTO() {
    }

    public StudentDTO(long id, String name, int age, String email) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.email = email;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                ", email='" + email + '\'' +
                '}';
    }
}
